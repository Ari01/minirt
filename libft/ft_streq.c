#include "libft.h"

int		ft_streq(char *s1, char *s2)
{
	size_t	len;

	len = ft_strlen(s1);
	if (len != ft_strlen(s2))
		return (0);
	if (!ft_strncmp(s1, s2, len))
		return (1);
	return (0);
}
